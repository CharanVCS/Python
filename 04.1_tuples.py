# Define a tuple named mytuple containing integers
mytuple = (1, 2, 4, 3, 4)

# Print the tuple
print(mytuple)

# Print the type of the variable mytuple
print(type(mytuple))

# Count the number of occurrences of the value 4 in the tuple
print(mytuple.count(4))

# Find the index of the first occurrence of the value 4 in the tuple
print(mytuple.index(4))
